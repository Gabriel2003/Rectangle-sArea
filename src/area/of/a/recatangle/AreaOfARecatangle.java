public class AreaOfARecatangle {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        // Calculating the area of a rectangle 
        
        double lenght = 4.8; 
        double width = 5.7;
        double area = 0.0; 
        // algorithm to calculate the area of the rectangle: 
        area = width * lenght; 
        System.out.println("area of a rectangle" + area + "cm squared");
    }
    
}
